package tangas;

public class RopaInterior {
	String marca;
	String tipoHilo;
	String color;
	double precio;
	boolean tieneDecoracion;
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getTipoHilo() {
		return tipoHilo;
	}
	public void setTipoHilo(String tipoHilo) {
		this.tipoHilo = tipoHilo;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public double getPrecio() {
		return precio;
	}
	public void setPrecio(double precio) {
		this.precio = precio;
	}
	public boolean isTieneDecoracion() {
		return tieneDecoracion;
	}
	public void setTieneDecoracion(boolean tieneDecoracion) {
		this.tieneDecoracion = tieneDecoracion;
	}	
}
